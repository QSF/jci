  <p>Tamires Quenupa não venceu mas se apresentou de forma brilhante na etapa nacional do projeto Oratória nas Escolas. 
    A representante de Londrina discursou sobre o Objetivo do Milênio 1, Acabar com a Fome e a Pobreza, apresentando dados sobre a fome no Brasil e 
    no mundo e convidando todos a agir para mudar esta situação. <br/><br/>
    "Estou aqui para falar que podemos mudar o mundo por meio de nossas ações", diz Tamires duas vezes em seu discurso, que durou 4 minutos e 54 segundos. 
    "Escolhi este tema pois é uma triste realidade que deve ser enfrentada. A fome mata mais pessoas que a AIDS, a Malária e a tuberculose juntas."</p>


  <p>A etapa nacional do projeto Oratória nas Escolas ocorreu durante a Convenção Nacional da JCI Brasil, em Gramado, no dia 12 de outubro. 
  Acompanhada de seu pai, Celso Quenupa, foi a primeira vez que Tamires viajou de avião. No último ensaio com a instrutora e coach Gislene Isquierdo 
  antes de ir a Gramado, Tamires respondeu à pergunta do antes e depois do projeto: antes - tímida, depois - uma guerreira, que vence mas também sabe perder. 
  Tamires também agradeceu: "Obrigada por me darem a oportunidade de participar deste projeto, de conhecer lugares diferentes como São Paulo e agora Gramado. 
  Obrigada por cada dia que passei com vocês. Aprendi muita coisa com vocês, aprendi a me comunicar muito bem. Obrigado por tudo que vocês me proporcionaram." </p>

